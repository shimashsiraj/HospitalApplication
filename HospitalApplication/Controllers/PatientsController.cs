using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using HospitalApplication.Data;
using HospitalApplication.Models;
using Microsoft.AspNetCore.Identity;

namespace HospitalApplication.Controllers
{
    public class PatientsController : Controller
    {
        private readonly ApplicationDbContext _context;

        public PatientsController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Patients
        public async Task<IActionResult> Index()
        {
            return View(await _context.Patients.ToListAsync());
        }

        // GET: Patients/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var patient = await _context.Patients
                .FirstOrDefaultAsync(m => m.Id == id);
            if (patient == null)
            {
                return NotFound();
            }

            return View(patient);
        }

        // GET: Patients/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: Patients/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,FirstName,LastName,Email,PhoneNumber")] Patient patient, string Password)
        {
            Console.WriteLine("🟡 POST method hit");

            if (!ModelState.IsValid)
            {
                Console.WriteLine("🔴 Model is invalid");
                foreach (var e in ModelState.Values.SelectMany(v => v.Errors))
                    Console.WriteLine("Model Error: " + e.ErrorMessage);

                return View(patient);
            }

            try
            {
                patient.PasswordHash = PasswordHash(Password);
                _context.Add(patient);
                await _context.SaveChangesAsync();
                Console.WriteLine("🟢 Patient saved to database");
                return RedirectToAction(nameof(Index));
            }
            catch (Exception ex)
            {
                Console.WriteLine("🔥 Exception: " + ex.Message);
                return View(patient);
            }
